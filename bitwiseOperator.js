    // ===== Bitwise AND ( & ) =====
console.log(5 & 3);   // 1
console.log(6 & 2);   // 2
console.log(15 & 9);  // 9

// ===== Bitwise OR ( | ) =====
console.log(5 | 3);   // 7
console.log(6 | 2);   // 6
console.log(15 | 9);  // 15

// ===== Bitwise XOR ( ^ ) =====
console.log(5 ^ 3);   // 6
console.log(6 ^ 2);   // 4
console.log(15 ^ 9);  // 6

// ===== Bitwise NOT ( ~ ) =====
console.log(~5);      // -6
console.log(~3);      // -4
console.log(~0);      // -1

// ===== Left Shift ( << ) =====
console.log(5 << 1);  // 10
console.log(5 << 2);  // 20
console.log(3 << 3);  // 24

// ===== Right Shift ( >> ) =====
console.log(5 >> 1);  // 2
console.log(20 >> 2); // 5
console.log(16 >> 3); // 2

// ===== Unsigned Right Shift ( >>> ) =====
console.log(5 >>> 1);  // 2
console.log(-1 >>> 0); // 4294967295
console.log(-8 >>> 1); // 2147483644